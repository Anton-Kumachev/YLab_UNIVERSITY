package lecture01.homework.task01;

/*
Напиши программу, которая выводит на экран надпись: «Слава Роботам! Убить всех человеков!» 16 раз.
*/

public class Solution09task01 {

    public static void main(String[] args) {
        String text = "Слава Роботам! Любить всех человеков!";
        for (int i = 0; i < 16; i++) {
            System.out.println(text);
        }
    }
}
